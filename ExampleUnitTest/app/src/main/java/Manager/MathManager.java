public class MathManager {
    public  float tambah(float value1, float value2)
    {
        return value1 + value2;
    }
    public  float kali(float value1, float value2)
    {
        return value1 * value2;
    }

    public  float bagi(float value1, float value2)
    {
        return value1 / value2;
    }

    public  float kurang(float value1, float value2)
    {
        return value1 - value2;
    }

}
